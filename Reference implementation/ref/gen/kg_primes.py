#!/usr/bin/env python3
"""Generate the keygen CRT prime table (and matching 2048-th roots) for Rhyme.

Primes are the largest 31-bit primes p with p = 1 (mod 2048), enumerated
downward from 2^31.  p = 1 (mod 2N) with N up to 1024 (=> 2N = 2048) is
required so each prime supports the length-N number-theoretic transform used
by the coprime-descent / module-NTRU solver (kg_ntt).  Each prime contributes
~31 bits to the CRT modulus; the table must be large enough that the widest
minor resultant (mode384/512 under the widened cut-F matrix) converges before
the table is exhausted.

For every prime we also emit one element of multiplicative order exactly 2048
(rhyme_kg_roots[]); kg_psi() derives every needed 2n-th root from it by
exponentiation, so any order-2048 element is correct and the choice does not
affect NTT/resultant outputs.
"""
import sys
from sympy import isprime, primitive_root

def gen(count):
    primes = []
    p = ((1 << 31) // 2048) * 2048 + 1
    if p >= (1 << 31):
        p -= 2048
    while len(primes) < count:
        if p.bit_length() == 31 and isprime(p):
            primes.append(p)
        p -= 2048
        if p <= 0:
            raise RuntimeError("ran out of 31-bit primes = 1 mod 2048")
    return primes

def root2048(p):
    g = primitive_root(p)                 # generator of Z_p^*
    w = pow(g, (p - 1) // 2048, p)        # order exactly 2048 (since 2048 | p-1)
    assert pow(w, 2048, p) == 1 and pow(w, 1024, p) != 1
    return w

def emit(count, path="src/keygen/kg_primes.c"):
    primes = gen(count)
    roots = [root2048(p) for p in primes]
    out = ["/* generated by gen/kg_primes.py: 31-bit primes p=1 mod 2048",
           "   (p = 1 mod 2N, N<=1024) with length-N NTT support, descending from 2^31,",
           "   plus one order-2048 root per prime for kg_psi(). */",
           "#include <stdint.h>",
           f"const unsigned rhyme_kg_nprimes = {count};",
           f"const uint32_t rhyme_kg_primes[{count}] = {{"]
    for i in range(0, count, 8):
        out.append("  " + ",".join(str(x) for x in primes[i:i+8]) + ",")
    out.append("};")
    out.append(f"const uint32_t rhyme_kg_roots[{count}] = {{")
    for i in range(0, count, 8):
        out.append("  " + ",".join(str(x) for x in roots[i:i+8]) + ",")
    out.append("};")
    open(path, "w").write("\n".join(out) + "\n")
    return primes, roots

if __name__ == "__main__":
    count = int(sys.argv[1]) if len(sys.argv) > 1 else 1200
    ps, rs = emit(count)
    print(f"wrote {count} primes+roots, max={ps[0]}, min={ps[-1]}, min.bitlen={ps[-1].bit_length()}")
